ലോകത്തിന്റെ വെളിച്ചമാണ് പുസ്തകങ്ങൾ ## മഹാത്മാഗാന്ധി
മരണം രംഗബോധമില്ലാത്ത കോമാളിയാണ് ## എം ടി (മഞ്ഞ്)
സംഘടന കൊണ്ട് ശക്തരാകുക, വിദ്യ കൊണ്ട് പ്രബുദ്ധരാകുക ## ശ്രീ നാരായണ ഗുരു
പുറംചട്ട കണ്ട് ഒരു പുസ്തകത്തെ വിലയിരുത്തരുത്. ## ഇംഗ്ലീഷ്  പഴമൊഴി
നാം അനുഭവിക്കാത്ത ജീവിതങ്ങള്‍ എല്ലാം നമുക്ക് വെറും കെട്ടുകഥകള്‍ മാത്രമാണ് ## ബെന്യാമിന്‍ (ആടുജീവിതം )
