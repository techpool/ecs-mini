"മറ്റുള്ള  ഭാഷകള്‍ കേവലം ധാത്രിമാര്‍ മര്‍ത്യന്നു പെറ്റമ്മ തന്‍ ഭാഷ താന്‍ " ## വള്ളത്തോള്‍
